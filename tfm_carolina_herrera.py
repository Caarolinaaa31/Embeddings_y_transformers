def extract_finetuned_embeddings(text_series, model, tokenizer, batch_size=32, max_len=256):
    embeddings = []
    text_list = text_series.tolist()

    for i in tqdm(range(0, len(text_list), batch_size), desc="Extrayendo Embeddings"):
        batch_texts = text_list[i:i + batch_size]

        # Tokenización
        inputs = tokenizer(
            batch_texts,
            padding="max_length",
            truncation=True,
            max_length=max_len,
            return_tensors="pt"
        ).to(device)

        with torch.no_grad():
            outputs = model(**inputs)

            # outputs.hidden_states es una tupla con las salidas de todas las capas.
            # Tomamos la última capa [-1]
            last_hidden_state = outputs.hidden_states[-1]

            # De toda la frase, nos quedamos solo con el token [CLS] (posición 0)
            cls_embeddings = last_hidden_state[:, 0, :].cpu().numpy().astype(np.float32)
            embeddings.append(cls_embeddings)

        # Limpieza de memoria GPU (muy importante para evitar OOM)
        del inputs, outputs, last_hidden_state, cls_embeddings
        torch.cuda.empty_cache()

    return np.vstack(embeddings)

def extract_t5_embeddings(text_series, model, tokenizer, batch_size=32, max_len=256):
    embeddings = []
    text_list = text_series.tolist()

    for i in tqdm(range(0, len(text_list), batch_size), desc="Extrayendo T5"):
        batch_texts = text_list[i:i + batch_size]

        inputs = tokenizer(
            batch_texts,
            padding="max_length",
            truncation=True,
            max_length=max_len,
            return_tensors="pt"
        ).to(device)

        with torch.no_grad():
            # Pasamos los textos solo por el Encoder de T5
            encoder_outputs = model.get_encoder()(**inputs)
            last_hidden_state = encoder_outputs.last_hidden_state

            # Hacemos Mean Pooling (promedio) ignorando los tokens de padding (ceros)
            attention_mask = inputs['attention_mask'].unsqueeze(-1).expand(last_hidden_state.size()).float()
            sum_embeddings = torch.sum(last_hidden_state * attention_mask, 1)
            sum_mask = attention_mask.sum(1)
            mean_pooled = sum_embeddings / torch.clamp(sum_mask, min=1e-9)

            embeddings.append(mean_pooled.cpu().numpy().astype(np.float32))

        del inputs, encoder_outputs, last_hidden_state, mean_pooled, attention_mask
        torch.cuda.empty_cache()

    return np.vstack(embeddings)

