def preprocess(text):
    """
    Elimina las fórmulas matemáticas, deja solo letras y todo en minúscula.
    """
    text = str(text)
    # Eliminar ecuaciones matemáticas
    text = re.sub(r'\$\$.*?\$\$|\$.*?\$', ' ', text, flags=re.DOTALL)
    # Eliminar caracteres que no sean letras y pasar a minúscula
    text = re.sub(r"[^A-Za-z]+", ' ', text).lower()
    # Eliminar espacios extra
    text = re.sub(r'\s+', ' ', text).strip()
    return text

def cleaning(doc):
    """
    Lematización y eliminación de stopwords.
    """
    txt = [token.lemma_ for token in doc if not token.is_stop]
    if len(txt) > 2: # Si el abstract quedó muy corto, se descarta
        return ' '.join(txt)
    return None

def preprocess_for_transformer(text):
    """
    Tranformar ecuaciones en "math_token".
    """
    text = str(text)
    text = re.sub(r'\$\$.*?\$\$|\$.*?\$', 'math_token', text, flags=re.DOTALL)
    text = re.sub(r'\s+', ' ', text).strip()
    return text

