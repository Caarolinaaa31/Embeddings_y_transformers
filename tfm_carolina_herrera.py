    np.save(ruta_train_w2v, X_train_w2v)
    np.save(ruta_test_w2v, X_test_w2v)
    np.save(ruta_train_w2v_scaled, X_train_w2v_scaled)
    np.save(ruta_test_w2v_scaled, X_test_w2v_scaled)

