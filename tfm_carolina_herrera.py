    # Ponemos 256 tokens para no saturar la RAM
    def get_bert_embeddings(text_series, batch_size=32, max_len=256):
        model_bert.eval()
        embeddings = []
        text_list = text_series.tolist()

        for i in tqdm(range(0, len(text_list), batch_size), desc="Extrayendo..."):
            batch_texts = text_list[i : i + batch_size]

            inputs = tokenizer(batch_texts, padding=True, truncation=True,
                               max_length=max_len, return_tensors="pt").to(device)

            with torch.no_grad(): # apagar el motor del cálculo del gradiente
                outputs = model_bert(**inputs)

            # token [CLS]
            cls_embeddings = outputs.last_hidden_state[:, 0, :].cpu().numpy().astype(np.float32) #Convertir directamente a float32 para ahorrar RAM
            embeddings.append(cls_embeddings)

            # Limpiar variables temporales de la GPU en cada iteración
            del inputs, outputs, cls_embeddings
            torch.cuda.empty_cache()

        return np.vstack(embeddings).astype(np.float32)

